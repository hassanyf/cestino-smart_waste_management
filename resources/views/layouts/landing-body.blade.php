            <h2 style="color:#212529">About CESTINO</h2>
            <p>Waste management is an important function of a smart and healthy city. In developing
              countries waste management is a big challenge. Current waste management mechanisms in
              Pakistan do not consider actual fill level from containers and instead schedule collection on
              predefined intervals. This is an inefficient way of collecting the waste as a lot of resources are
              required to gather half or less filled containers.
              Moreover, as the generation of trash is dynamic in nature, it often results in some trash cans
              getting overloaded and leaking while other remain almost empty. An overflowing trash can be
              not only aesthetically unpleasant but it may also be a health hazard for communities, especially
              in warm environments.</p><p>
              A trash can is one of those things, which hardly gets any attention until it is full and the trash
              seems to spill out of it. So the most obvious way to make it smart would be to detect if it is
              getting full and then send a notification to the responsible waste management companies. We
              present to you our solution CESTINO which can be used to build a smart trash can monitoring
              system such that the waste management companies gets notified as soon as the bin is full.</p><p>
              In order to measure the trash fill level we are collecting the data from sensors. These sensors and related hardware will be installed on the trash containers.</p>
